
public class Animal {
	private String color;
	private String speace;
	protected int age;
	private static int numAnim;
	
	public Animal(String color,String speace,int age){
		this.color = color; 
		this.speace = speace;
		this.age = age;
		numAnim++;
	}
	
	public String getColor(){
		return this.color;
	}
	
	public int getAge(){
		return this.age;
	}
	
	public static int getNum(){
		return numAnim;
	}
	
	
}
